#!/usr/bin/python3

def add(a, b):
    """Ela's addition function

    Arguments:
        a: the first integer
        b: the second integer

    Returns:
        The return value of. a + b
    """

    return (a + b)


def sub(a, b):
    """Ela's subtraction function

    Arguments:
        a: the first integer
        b: the second integer

    Returns:
        The return value of. a - b
    """

    return (a - b)


def mul(a, b):
    """Ela's multiplication function

    Arguments:
        a: the first integer
        b: the second integer

    Returns:
        The return value of. a * b
    """

    return (a * b)


def div(a, b):
    """ela's division function

    Arguments:
        a: the first integer
        b: the second integer

    Returns:
        The return value of. a / b
    """

    return int(a / b)

